package lecture0hellojava;


// Class is collection of
// Methods
public class HelloWorld {

    // Main method
    // This one method is the only method
    // where CPU will read and execute the instructions
    public static void main(String[] args) {

        // Our first instruction to CPU
        // is to display "Hello World" message
        // to the Terminal Screen
        System.out.println("Hello World!");

        // another variant of same instruction will
        // display the "Hello World" text to the terminal window
        // and also moves the courser to the new line
        System.out.println("Hello World");
    }
}
